namespace RawDeal._0._PreGame.Cards;

public class SuperStarCardInfo {
    public string Name;
    public string Logo;
    public int HandSize;
    public int SuperStarValue;
    public string SuperStarAbility;
    
    public SuperStarCardInfo(string name, string logo, int handSize, int superStarValue, string superStarAbility) {
        Name = name;
        Logo = logo;
        HandSize = handSize;
        SuperStarValue = superStarValue;
        SuperStarAbility = superStarAbility;
    }
    
}